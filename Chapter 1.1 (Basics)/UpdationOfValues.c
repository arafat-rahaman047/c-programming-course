#include <stdio.h>
int main() {
    int x; // declaration
    x=5; // initialization
    printf("%d\n",x);

    x=10; // updating the value of x
    printf("%d\n",x);

    x = x+100;
    printf("%d\n",x);

    x = x-20;
    printf("%d\n",x);

    x = x*5;
    printf("%d\n",x);

    x = x/50;
    printf("%d\n",x);

    return 0;
}